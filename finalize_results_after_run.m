% Final post-processing after simulations have completed.
% This script refreshes figure exports, checks completeness, collects summary
% tables, and writes a compact key-finding report.

clear; clc;

fprintf('\n=== Refresh baseline figures ===\n');
if exist('baseline_result.mat', 'file')
    tmp = load('baseline_result.mat', 'result');
    if is_current_result(tmp.result)
        regenerate_baseline_figures;
    else
        fprintf('baseline_result.mat was generated by an old model revision. Run generate_all_outputs first.\n');
    end
else
    fprintf('baseline_result.mat not found. Skipping baseline figure refresh.\n');
end

fprintf('\n=== Refresh variable-gap sensitivity figures ===\n');
if exist('variable_gap_sensitivity_result.mat', 'file')
    tmp = load('variable_gap_sensitivity_result.mat', 'results');
    if ~isempty(tmp.results) && is_current_result(tmp.results{1})
        regenerate_variable_gap_sensitivity_figures;
    else
        fprintf('variable_gap_sensitivity_result.mat was generated by an old model revision. Run generate_all_outputs first.\n');
    end
else
    fprintf('variable_gap_sensitivity_result.mat not found. Skipping variable-gap sensitivity figures.\n');
end

fprintf('\n=== Refresh fine temperature contours ===\n');
if exist('temperature_field_fine_result.mat', 'file')
    close all;
    load('temperature_field_fine_result.mat', 'result');
    if is_current_result(result)
        for k = 1:numel(result.snapshots.time)
            plot_temperature_contour(result, k, round(result.param.Nx_pipe / 2));
        end
        save_all_open_figures(fullfile('figures', 'temperature_field_fine'), 'contour_fine');
        close all;
    else
        fprintf('temperature_field_fine_result.mat was generated by an old model revision. Run generate_all_outputs first.\n');
    end
else
    fprintf('temperature_field_fine_result.mat not found. Skipping fine contour refresh.\n');
end

fprintf('\n=== Check generated results ===\n');
check_generated_results;

fprintf('\n=== Collect summary tables ===\n');
collect_summary_tables;

fprintf('\n=== Extract key findings ===\n');
extract_key_findings;

fprintf('\nFinal post-processing complete.\n');

function tf = is_current_result(result)
    tf = isfield(result, 'param') ...
        && isfield(result.param, 'model_revision') ...
        && strcmp(result.param.model_revision, 'soil_phase_offset_v2');
end
