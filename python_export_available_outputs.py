"""Fallback exporter for EAHE results when MATLAB cannot start.

This script reads existing .mat/.csv result files and regenerates PNG figures
in the existing figures/* folders. It also exports summary tables and a compact
key-finding text file. MATLAB .fig files are not generated by this fallback.
"""

import math
from pathlib import Path

import matplotlib.pyplot as plt
import numpy as np
import pandas as pd
import scipy.io as sio
from scipy.interpolate import griddata


ROOT = Path(__file__).resolve().parent
FIGROOT = ROOT / "figures"
CURRENT_REVISION = "soil_phase_offset_v2"


def loadmat(name: str) -> dict:
    return sio.loadmat(ROOT / name, squeeze_me=True, struct_as_record=False)


def arr(x):
    return np.asarray(x).squeeze()


def ensure_clean(folder: Path):
    folder.mkdir(parents=True, exist_ok=True)
    for path in folder.glob("*.png"):
        path.unlink()


def savefig(folder: str, filename: str):
    path = FIGROOT / folder
    path.mkdir(parents=True, exist_ok=True)
    plt.tight_layout()
    plt.savefig(path / filename, dpi=300)
    plt.close()


def result_list(obj):
    if isinstance(obj, np.ndarray):
        return [obj.flat[i] for i in range(obj.size)]
    return [obj]


def result_revision(result) -> str:
    try:
        return str(result.param.model_revision)
    except Exception:
        return "<unknown>"


def is_current_result(result) -> bool:
    return result_revision(result) == CURRENT_REVISION


def require_current(result, name: str) -> bool:
    rev = result_revision(result)
    if rev != CURRENT_REVISION:
        print(f"Skipping {name}: result revision is {rev}, expected {CURRENT_REVISION}.")
        return False
    return True


def plot_baseline():
    p = ROOT / "baseline_result.mat"
    if not p.exists():
        return
    ensure_clean(FIGROOT / "baseline")
    r = loadmat(p.name)["result"]
    if not require_current(r, p.name):
        return
    t = arr(r.time) / 3600
    idx = np.arange(1, len(t))

    plt.figure(figsize=(12, 6))
    plt.plot(t[idx], arr(r.Tin)[idx], "k--", lw=1.2, label="Inlet air")
    plt.plot(t[idx], arr(r.Tout)[idx], "b-", lw=1.5, label="Outlet air")
    plt.xlabel("Time / h")
    plt.ylabel("Temperature / degC")
    plt.grid(True, alpha=0.35)
    plt.legend()
    savefig("baseline", "baseline_01_EAHE_inlet_and_outlet_temperature.png")

    plt.figure(figsize=(12, 6))
    plt.plot(t[idx], arr(r.Q)[idx], "r-", lw=1.5)
    plt.xlabel("Time / h")
    plt.ylabel("Heat transfer rate / W")
    plt.grid(True, alpha=0.35)
    savefig("baseline", "baseline_02_EAHE_heat_transfer_rate.png")

