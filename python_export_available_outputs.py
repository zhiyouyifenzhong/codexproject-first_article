def mat_file_is_current(mat_name: str) -> bool:
    path = ROOT / mat_name
    if not path.exists():
        return False
    try:
        data = loadmat(mat_name)
        if "result" in data:
            return is_current_result(data["result"])
        if "results" in data:
            cases = result_list(data["results"])
            return bool(cases) and is_current_result(cases[0])
        if "case_gap" in data:
            return is_current_result(data["case_gap"])
        if "fixed_case" in data:
            return is_current_result(data["fixed_case"])
        for key in ("m_results", "L_results", "dt_results", "Nx_results", "Ntheta_results", "Nr_results"):
            if key in data:
                cases = result_list(data[key])
                return bool(cases) and is_current_result(cases[0])
    except Exception:
        return False
    return False


def export_key_findings():
    lines = ["EAHE Key Findings", "=================", ""]
    if (ROOT / "baseline_result.mat").exists():
        r = loadmat("baseline_result.mat")["result"]
        if is_current_result(r):
            lines += [
                "Baseline case",
                f"- Final outlet temperature: {float(arr(r.Tout)[-1]):.2f} degC",
                f"- Last-day mean heat transfer: {float(arr(r.degradation.Q_day_mean)[-1]):.1f} W",
                f"- Last-day heat-transfer degradation ratio: {float(arr(r.degradation.eta_Q_day)[-1]):.3f}",
                f"- Last-day near-pipe mean soil temperature rise: {float(arr(r.degradation.near_soil_rise_day_mean)[-1]):.2f} K",
                "",
            ]
        else:
            lines += [
                "Baseline case",
                f"- Skipped: baseline_result.mat revision is {result_revision(r)}, expected {CURRENT_REVISION}.",
                "",
            ]
    csv_to_mat = {
        "degradation_validation_summary.csv": "degradation_validation_result.mat",
        "gap_sensitivity_summary.csv": "gap_sensitivity_result.mat",
        "gap_conductivity_sensitivity_summary.csv": "gap_conductivity_sensitivity_result.mat",
        "operation_mdot_summary.csv": "operation_sensitivity_result.mat",
        "operation_length_summary.csv": "operation_sensitivity_result.mat",
        "soil_layer2_sensitivity_summary.csv": "soil_layer2_sensitivity_result.mat",
        "variable_gap_extension_summary.csv": "variable_gap_extension_result.mat",
        "variable_gap_sensitivity_summary.csv": "variable_gap_sensitivity_result.mat",
        "validation_dt_summary.csv": "numerical_validation_result.mat",
        "validation_Nx_summary.csv": "numerical_validation_result.mat",
        "validation_Ntheta_summary.csv": "numerical_validation_result.mat",
        "validation_Nr_summary.csv": "numerical_validation_result.mat",
    }
    for csv_name, mat_name in csv_to_mat.items():
        csv = ROOT / csv_name
        if not csv.exists():
            continue
        lines.append(csv.name)
        lines.append("-" * len(csv.name))
        if mat_file_is_current(mat_name):
            df = pd.read_csv(csv)
            lines.append(df.to_string(index=False))
        else:
            lines.append(f"Skipped: {mat_name} is missing or not current revision.")
        lines.append("")
    (ROOT / "EAHE_key_findings.txt").write_text("\n".join(lines), encoding="utf-8")


