def idx_ts(i, m, r, param):
    nseg = int(param.nNode_seg)
    nr = int(param.Nr)
    return (i - 1) * nseg + 2 + (m - 1) * nr + r - 1


def plot_temperature_contours():
    p = ROOT / "temperature_field_fine_result.mat"
    if not p.exists():
        return
    ensure_clean(FIGROOT / "temperature_field_fine")
    result = loadmat(p.name)["result"]
    if not require_current(result, p.name):
        return
    param = result.param
    geom = result.geom
    times = np.atleast_1d(arr(result.snapshots.time))
    Xmat = np.asarray(result.snapshots.X)
    if Xmat.ndim == 1:
        Xmat = Xmat[:, None]

    nx = int(param.Nx_pipe)
    ntheta = int(param.Ntheta)
    nr = int(param.Nr)
    mid = round(nx / 2)
    theta = arr(geom.theta)
    rcenters = arr(geom.r_centers)
    ro = float(param.r_o)
    rmax = float(param.r_soil_max)
    zpipe = float(param.z_pipe)

    for k, time_s in enumerate(times):
        X = Xmat[:, k]
        xs, zs, ts = [], [], []
        for mi in range(1, ntheta + 1):
            th = theta[mi - 1]
            for ri in range(1, nr + 1):
                rr = rcenters[ri - 1]
                xs.append(rr * math.cos(th))
                zs.append(zpipe + rr * math.sin(th))
                ts.append(X[idx_ts(mid, mi, ri, param)])
        xs, zs, ts = map(np.asarray, (xs, zs, ts))

        xq = np.linspace(-rmax, rmax, 260)
        zq = np.linspace(zpipe - rmax, zpipe + rmax, 260)
        Xq, Zq = np.meshgrid(xq, zq)
        Tq = griddata((xs, zs), ts, (Xq, Zq), method="cubic")
        if np.isnan(Tq).any():
            Tlin = griddata((xs, zs), ts, (Xq, Zq), method="linear")
            Tq = np.where(np.isnan(Tq), Tlin, Tq)
        Rq = np.sqrt(Xq**2 + (Zq - zpipe) ** 2)
        Tq[(Rq < ro) | (Rq > rmax) | (Zq < 0)] = np.nan

        plt.figure(figsize=(8, 9))
        cf = plt.contourf(Xq, Zq, Tq, 32, cmap="viridis")
        plt.colorbar(cf, label="Temperature / degC")
        th = np.linspace(0, 2 * np.pi, 300)
        plt.plot(ro * np.cos(th), zpipe + ro * np.sin(th), "k-", lw=2)
        for zb, lab in zip(arr(param.z_layer_bot)[:2], ["Layer 1/2", "Layer 2/3"]):
            plt.axhline(zb, color="k", ls="--", lw=1, alpha=0.65)
            plt.text(-rmax * 0.98, zb, lab, va="bottom", ha="left", fontsize=9)
        plt.gca().invert_yaxis()
        plt.axis("equal")
        plt.xlabel("Horizontal distance from pipe center / m")
        plt.ylabel("Depth / m")
        plt.title(f"Near-pipe soil temperature, t = {time_s/3600:.1f} h, segment = {mid}")
        savefig("temperature_field_fine", f"contour_fine_{k+1:02d}_Temperature_contour_{time_s/3600:.1f}_h.png")


